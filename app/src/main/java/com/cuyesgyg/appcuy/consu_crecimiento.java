class consu_crecimiento {

    private String cant_crecimiento;
    private String fecha_inicio;
    private String fecha_peso;
    private String peso;

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getFecha_peso() {
        return fecha_peso;
    }

    public void setFecha_peso(String fecha_peso) {
        this.fecha_peso = fecha_peso;
    }

    public String getCant_crecimiento() {
        return cant_crecimiento;
    }

    public void setCant_crecimiento(String cant_crecimiento) {
        this.cant_crecimiento = cant_crecimiento;
    }

    public String getFecha_inicio() {
        return fecha_inicio;
    }

    public void setFecha_inicio(String fecha_inicio) {
        this.fecha_inicio = fecha_inicio;
    }

}
